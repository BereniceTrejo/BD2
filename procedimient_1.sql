--A)CREAR TABLA DANCE
CREATE TABLE DANCE (ID INTEGER PRIMARY KEY, MENSAJE VARCHAR2(80));
/*B) CREAR UN PROCEDIMIENTO ALMACENADOQUE GUARDE UN REGISTROEN ESA TABLA, 
DALE EL NOMBRE DE GUARDAR_DANCE.
EN ESTE CASO COMO NO SE AUTO GENERA EL PRIMARY KEY LOS DOS ARGUMENTOS DEBEN DE SER
DE TIPO DE ENTRADA "IN"*/
CREATE OR REPLACE PROCEDURE GUARDAR_DANCE(VAL1 IN INTEGER,VAL2 IN VARCHAR2)
AS
  BEGIN
  INSERT INTO DANCE VALUES(VAL1,VAL2);
  END;
  /
--PROBAMOS INTERNAMENTE
BEGIN
