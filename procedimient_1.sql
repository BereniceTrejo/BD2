/*
EJERCICIO 1

CREAR UN PROCEDIMIENTO QUE RECIBE UN MENSAJE Y QUE LO IMPRIMA AL INVOCARLO, DARLE EL NOMBRE DE IMPRIMIR
*/

SET SERVEROUTPUT ON;

CREATE OR REPLACE PROCEDURE IMPRIMIR(MENSA IN VARCHAR2)
AS
BEGIN
DBMS_OUTPUT.PUT_LINE(MENSA);
END;
/

--VAMOS A INVOCARLO INTERNAMENTE,ES DECIR DESDE AQUI EN ORACLE
BEGIN
IMPRIMIR('VAMOS A VER SI FUNCIONA');
END;
/

